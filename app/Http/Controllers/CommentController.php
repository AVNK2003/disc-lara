    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Comment $comment)
    {
        $comment->delete();
        return redirect()->route('comment.index')->with('success', 'Комментарий успешно удален');
    }

    public function togglePublish(Comment $comment)
    {
        $comment->published = !$comment->published;
        $comment->save();
        $status = $comment->published ? 'Комментарий опубликован' : 'Комментарий снят с публикации';
        return redirect()->route('comment.index')->with('success', $status);
    }
